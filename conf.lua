
function love.config(t)
    t.console = true
    t.window.title = "Game Name"

   -- Using 1080p (for now)
    t.window.width = 2560  
    t.window.height = 1440

    -- window display
    t.window.display = 2
    t.window.resizable = true
    t.window.fullscreen = false
    t.window.borderless = false


    t.window.title = "Resizable Window"
    
end